package com.example.EazyPG.owner.Activities.FoodDetails;

public class TuesdayFoodDetails {

    public String breakfast, lunch, dinner;

    public TuesdayFoodDetails() {
        breakfast = "";
        lunch = "";
        dinner = "";
    }

    public TuesdayFoodDetails(String breakfast, String lunch, String dinner) {
        this.breakfast = breakfast;
        this.lunch = lunch;
        this.dinner = dinner;
    }
}
