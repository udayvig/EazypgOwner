package com.eazypg.EazyPG.owner.Activities.FoodDetails;

public class FridayFoodDetails {

    public String breakfast, lunch, dinner;

    public FridayFoodDetails() {
        breakfast = "";
        lunch = "";
        dinner = "";
    }

    public FridayFoodDetails(String breakfast, String lunch, String dinner) {
        this.breakfast = breakfast;
        this.lunch = lunch;
        this.dinner = dinner;
    }
}
